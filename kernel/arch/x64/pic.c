#include <x64/pic.h>
#include <x64/asm.h>

// Disables PIC. We use IO APIC instead.
void x64_pic_init(void) {

    asm_out8(0xa1, 0xff);
    asm_out8(0x21, 0xff);

    asm_out8(0x20, 0x11);
    asm_out8(0xa0, 0x11);
    asm_out8(0x21, 0x20);
    asm_out8(0xa1, 0x28);
    asm_out8(0x21, 0x04);
    asm_out8(0xa1, 0x02);
    asm_out8(0x21, 0x01);
    asm_out8(0xa1, 0x01);

    asm_out8(0xa1, 0xff);
    asm_out8(0x21, 0xff);

}
